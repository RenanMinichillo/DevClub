/*
    Switch Case - Controlador de Fluxo
*/

const temperatura = 30

switch (temperatura) {
    case 10:
        console.log('Está muito frio')
        break;

    case 20:
        console.log('Está frio')
        break;

    case 30:
        console.log('Está quente')
        break;

    default:
        console.log('Não sei te dizer')
        break;
}